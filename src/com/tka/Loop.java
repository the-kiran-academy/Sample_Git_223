package com.tka;

public class Loop {

	public static void printTable() {
		for (int i = 1; i <= 10; i++) {
			System.out.println("2 * " + i + " = " + (2 * i));
		}

	}

	public static void printTableThree() {
		for (int i = 1; i <= 10; i++) {
			System.out.println("3 * " + i + " = " + (3 * i));
		}
	}

	public static void printTableThirteen() {
		for (int i = 1; i <= 10; i++) {
			System.out.println("13 * " + i + " = " + (13 * i));
		}
	}

}
