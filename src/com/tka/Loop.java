package com.tka;

public class Loop {
	
	public static void printTable() {
		for (int i = 1; i <= 10; i++) {
			System.out.println("2 * " + i + " = " + (2 * i));
		}
		
	}
	public static void printTableSeven() {
		for (int i = 1; i <= 10; i++) {
			System.out.println("7 * " + i + " = " + (7 * i));
		}
	}

	
	

}
